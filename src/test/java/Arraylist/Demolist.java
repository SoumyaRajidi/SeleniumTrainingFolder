public class Demolist {
	int testcasecount;
	String testcasename;
	String browsername;
	String url;
	boolean testcasestatus;

	Demolist(int testcasecount, String testcasename, String browsername, String url, boolean testcasestatus) {
		this.testcasecount = testcasecount;
		this.testcasename = testcasename;
		this.browsername = browsername;
		this.url = url;
		this.testcasestatus=testcasestatus;

	}

}
